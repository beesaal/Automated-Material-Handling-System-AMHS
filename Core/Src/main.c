// Function to clear the terminal screen
void ClearScreen() {
  // ANSI escape code to clear the screen and move cursor to home position
  const char *clearCommand = "\033[2J\033[H";
  HAL_UART_Transmit(&huart2, (uint8_t*) clearCommand, strlen(clearCommand),
  HAL_MAX_DELAY);
}

void ButtonPressed_Fn(void *argument) {
  for (;;) {
    if (HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13) == GPIO_PIN_RESET) {
      ClearScreen();
      vTaskDelay(200);
    }
  }
}
